// Each slot drives the card border, bg tint, dot, and badge colour
function s(hex, r, g, b) {
  return {
    dot:    hex,
    bg:     `rgba(${r},${g},${b},0.07)`,
    border: `rgba(${r},${g},${b},0.20)`,
    light:  `rgba(${r},${g},${b},0.11)`,
  };
}

